const mongoose = require("mongoose");

const newsEventsSchema = new mongoose.Schema({
  id: {
    type: String,
    required: true,
    unique: true,
  },
  description: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model("NewsEvents", newsEventsSchema);
